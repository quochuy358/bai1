$isEqual = $num1 == $num2; 
echo '$num1 == $num2: ' . ($isEqual ? 'true' : 'false') . '<br>';

$isNotEqual = $num1 != $num2; 
echo '$num1 != $num2: ' . ($isNotEqual ? 'true' : 'false');
?>
